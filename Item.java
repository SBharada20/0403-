package pla9_10;

public class Item {
	private String Name;
	private int Price;
	private int Weight;
	
	public String getName() {
		return this.Name;
	}
	public void setName(String Name) {
		this.Name = Name;
	}
	
	public int getPrice() {
		return this.Price;
	}
	public void setPrice(int Price) {
		this.Price = Price;
	}
	
	public int getWeight() {
		return this.Weight;
	}
	public void setWeight(int Weight) {
		this.Weight = Weight;
	}
	

}
